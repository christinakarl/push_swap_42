#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*hu;

	if (!(*lst) || !del)
		return ;
	while (*lst)
	{
		hu = (*lst)->next;
		ft_lstdelone(*lst, del);
		(*lst) = hu;
	}
	(*lst) = NULL;
}
